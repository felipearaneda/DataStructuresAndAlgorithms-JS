
for (var i=0; i<10; i++) {
    console.log(i);
}

var i = 0;

while(i<10){
    console.log(i);
    i++;
}

var i = 0;
do {
    console.log(i);
    i++;
} while (i<10)

/*
The do…while loop is very similar to the while loop. The only difference is that in
the while loop, the condition is evaluated before executing the script, and in the do…
while loop, the condition is evaluated after the script is executed.
 */